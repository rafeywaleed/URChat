  void _showEmojiPicker() {
    showDialog(
      context: context,
      builder: (context) => NesDialog(
        child: Container(
          width: 400,
          height: 500,
          child: Column(
            children: [
              NesContainer(
                child: Text(
                  'Select Emoji',
                  style: Theme.of(context).textTheme.headlineSmall,
                ),
              ),
              SizedBox(height: 16),
              Expanded(
                child: GridView.builder(
                  gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
                    crossAxisCount: 6,
                    crossAxisSpacing: 8,
                    mainAxisSpacing: 8,
                  ),
                  itemCount: emojis.length,
                  itemBuilder: (context, index) {
                    return NesButton(
                      type: NesButtonType.normal,
                      onPressed: () {
                        setState(() {
                          _selectedEmoji = emojis[index];
                        });
                        widget.onEmojiChanged(_selectedEmoji);
                        Navigator.pop(context);
                      },
                      child: Container(
                        padding: EdgeInsets.all(8),
                        child: Text(
                          emojis[index],
                          style: TextStyle(fontSize: 20),
                        ),
                      ),
                    );
                  },
                ),
              ),
              SizedBox(height: 16),
              NesButton(
                type: NesButtonType.normal,
                onPressed: () => Navigator.pop(context),
                child: Text('Close'),
              ),
            ],
          ),
        ),
      ),
    );
  }

  void _showColorPicker() {
    showDialog(
      context: context,
      builder: (context) => NesDialog(
        child: Container(
          width: 350,
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              NesContainer(
                child: Text(
                  'Select Background Color',
                  style: Theme.of(context).textTheme.headlineSmall,
                ),
              ),
              SizedBox(height: 16),

              // Current Selection Preview
              Container(
                width: 80,
                height: 80,
                decoration: BoxDecoration(
                  color: _selectedColor,
                  borderRadius: BorderRadius.circular(8),
                  border: Border.all(color: Colors.black, width: 2),
                ),
                child: Center(
                  child: Text(
                    _selectedEmoji,
                    style: TextStyle(fontSize: 32),
                  ),
                ),
              ),
              SizedBox(height: 16),

              // Default Colors
              Text('Default Colors',
                  style: Theme.of(context).textTheme.titleMedium),
              SizedBox(height: 12),
              Container(
                height: 120,
                child: GridView.builder(
                  gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
                    crossAxisCount: 8,
                    crossAxisSpacing: 8,
                    mainAxisSpacing: 8,
                  ),
                  itemCount: defaultColors.length,
                  itemBuilder: (context, index) {
                    return GestureDetector(
                      onTap: () {
                        setState(() {
                          _selectedColor = defaultColors[index];
                        });
                        widget.onColorChanged(_selectedColor);
                        Navigator.pop(context);
                      },
                      child: Container(
                        decoration: BoxDecoration(
                          color: defaultColors[index],
                          border: Border.all(
                            color: _selectedColor == defaultColors[index]
                                ? Colors.white
                                : Colors.transparent,
                            width: 3,
                          ),
                          borderRadius: BorderRadius.circular(4),
                        ),
                        child: _selectedColor == defaultColors[index]
                            ? Icon(Icons.check, color: Colors.white, size: 16)
                            : null,
                      ),
                    );
                  },
                ),
              ),

              SizedBox(height: 16),
              NesButton(
                type: NesButtonType.normal,
                onPressed: () => Navigator.pop(context),
                child: Text('Close'),
              ),
            ],
          ),
        ),
      ),
    );
  }

